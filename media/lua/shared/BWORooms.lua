BWORooms.IsIntrusion = function(room)

    -- available professions types: 
    -- unemployed, fireofficer, policeofficer, parkranger, constructionworker, securityguard, carpenter, burglar, chef, farmer, fisherman
    -- doctor, veteran, nurse, lumberjack, fitnessinstructor, burgerflipper, electrician, metalworker, mechanics
    local player = getPlayer()
    local profession = player:getDescriptor():getProfession()
    local building = room:getBuilding()
    local roomName = room:getName()

    if BWOBuildings.IsEventBuilding(building, "party") then return false end

    if BWOBuildings.IsEventBuilding(building, "home") then return false end

    -- if BWOBuildings.IsResidential(building) then return true end

    if BWORooms.IsEmpty(room) then return false end

    if BWORooms.IsShop(room) then return false end

    if BWORooms.IsRestaurant(room) then return false end
    
    if roomName == "bathroom" or roomname == "church" then return false end

    local isIntrusion = true
    local name = room:getName()
    local tab = BWORooms.tab
    local data = BWORooms.tab[name]
    if data.isStorage then
        if data.occupations then
            for _, occupation in pairs(data.occupations) do
                if profession == occupation then
                    isIntrusion = false
                    break
                end
            end
        end
    end

    return isIntrusion
end

BWORooms.TakeIntention = function(room)
    local canTake = false
    local shouldPay = false

    local building = room:getBuilding()

    if BWOBuildings.IsEventBuilding(building, "party") then 
        canTake = true
        shouldPay = false

    elseif BWOBuildings.IsEventBuilding(building, "home") then 
        canTake = true
        shouldPay = false

    elseif BWOBuildings.IsResidential(building) then 
        canTake = false
        shouldPay = false

    elseif BWORooms.IsShop(room) then
        canTake = false
        shouldPay = true

    elseif BWORooms.IsRestaurant(room) then
        canTake = false
        shouldPay = true

    end

    return canTake, shouldPay
end

BWORooms.GetRoomSize = function(room)
    local roomDef = room:getRoomDef()
    local size = (roomDef:getX2() - roomDef:getX()) * (roomDef:getY2() - roomDef:getY())
    return size
end

BWORooms.GetRoomPopMod = function(room)
    local popMod = 1
    local gameTime = getGameTime()
    local hour = gameTime:getHour()
    local minute = gameTime:getMinutes()
    local building = room:getBuilding()
    local roomName = room:getName()
    
    if BWOBuildings.IsResidential(building) then 
        if hour < 6 then
            popMod = 2
        elseif hour < 17 then
            popMod = 0.5
        elseif hour < 19 then
            popMod = 0.75
        elseif hour < 21 then
            popMod = 1.2
        else
            popMod = 1.6
        end
    elseif BWORooms.IsStorage(room) then
        popMod = 0.5
    elseif BWORooms.IsShop(room) then
        if hour < 8 then
            popMod = 0
        elseif hour < 10 then
            popMod = 0.5
        elseif hour < 19 then
            popMod = 1.2
        elseif hour < 20 then
            popMod = 0.5
        else
            popMod = 0
        end
    elseif BWORooms.IsRestaurant(room) then
        if hour < 8 then
            popMod = 0
        elseif hour < 12 then
            popMod = 0.8
        elseif hour < 14 then
            popMod = 1.6
        elseif hour < 19 then
            popMod = 0.7
        elseif hour < 23 then
            popMod = 2
        elseif hour < 24 then
            popMod = 0.8
        end
    elseif BWORooms.IsEmpty(room) then
        popMod = 0.1
    elseif roomName == "church" or roomName == "classroom" then
        if hour < 6 then
            popMod = 0
        elseif hour < 18 then
            popMod = 2
        else
            popMod = 0
        end
    end
    return popMod
end